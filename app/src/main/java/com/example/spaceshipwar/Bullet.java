package com.example.spaceshipwar;

import android.graphics.Bitmap;

public class Bullet {
    private float x;
    private float y;


    public Bullet(float x, float y) {
        this.x = x;
        this.y = y;
    }


    public float getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}

